class Solution:
    def pacificAtlantic(self, matrix):
        """
        :type matrix: List[List[int]]
        :rtype: List[List[int]]
        """
        if not matrix:
            return []
        m, n = len(matrix), len(matrix[0])
        p_visited = [[False for _ in range(n)] for __ in range(m)]
        a_visited = [[False for _ in range(n)] for __ in range(m)]

        def dfs(i, j, visited):
            directions = [[0, 1], [0, -1], [1, 0], [-1, 0]]
            visited[i][j] = True
            for direction in directions:
                if 0 <= direction[0] + i < m and 0 <= direction[1] + j < n and not visited[direction[0] + i][
                            direction[1] + j] and matrix[i][j] < matrix[direction[0] + i][direction[1] + j]:
                    dfs(direction[0] + i, direction[1] + j, visited)
        for i in range(m):
            dfs(i,0,p_visited)
            dfs(i,n-1,a_visited)
        for j in range(n):
            dfs(0,j,p_visited)
            dfs(m-1,j,a_visited)
        res = []
        for i in range(m):
            for j in range(n):
                if p_visited[i][j] and a_visited[i][j]:
                    res.append([i,j])
        return res

